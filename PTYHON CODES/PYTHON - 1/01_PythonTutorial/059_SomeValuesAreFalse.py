#Some Values are False
'''In fact, there are not many values that evaluate to False, except empty values,
 such as (), [], {}, "", 
 the number 0, and the value None. And of course the value False evaluates to False.'''

#The following will return False:

print(bool(False))
print(bool(None))
print(bool(0))
print(bool(""))
print(bool(()))
print(bool([]))
print(bool({}))